// writeCursorAtomic is shared between AppendHistory and WriteCursor.
func (m *MemoryStore) writeCursorAtomic(c int) error {
	target := filepath.Join(m.root, ".cursor")
	return atomicWrite(target, []byte(fmt.Sprintf("%d\n", c)))
}

// atomicWrite writes payload to target via a same-directory temp file and
// os.Rename. The temp file is deleted on error; on success it's the only
// artifact left behind (replacing target).
func atomicWrite(target string, payload []byte) error {
	dir := filepath.Dir(target)
	tmp, err := os.CreateTemp(dir, filepath.Base(target)+".tmp-*")
	if err != nil {
		return fmt.Errorf("atomicWrite tmpfile: %w", err)
	}
	tmpName := tmp.Name()
	cleanup := func() { _ = os.Remove(tmpName) }

	if _, err := tmp.Write(payload); err != nil {
		_ = tmp.Close()
		cleanup()
		return fmt.Errorf("atomicWrite write: %w", err)
	}
	if err := tmp.Sync(); err != nil {
		// fsync failure is unusual but tolerable on most ephemeral FSs;
		// don't make it fatal in tests / CI environments.
		_ = err
	}
	if err := tmp.Close(); err != nil {
		cleanup()
		return fmt.Errorf("atomicWrite close: %w", err)
	}
	if err := os.Rename(tmpName, target); err != nil {
		cleanup()
		return fmt.Errorf("atomicWrite rename: %w", err)
	}
	return nil
}

// countLines returns the number of '\n'-terminated lines in path.
// A file ending without a trailing newline still counts the last line.
// Empty file → 0. Missing file → 0 with no error (callers can treat that
// as "fresh agent").
func (m *MemoryStore) countLines(path string) (int, error) {
	f, err := os.Open(path)
	if err != nil {
		if os.IsNotExist(err) {
			return 0, nil
		}
		return 0, err
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	// Allow long JSON lines — default 64KB token cap is too small for
	// large tool-result content blocks.
	scanner.Buffer(make([]byte, 64*1024), 4*1024*1024)
	count := 0
	for scanner.Scan() {
		count++
	}
	if err := scanner.Err(); err != nil {
		return 0, err
	}
	return count, nil
}
